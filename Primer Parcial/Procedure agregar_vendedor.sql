CREATE PROCEDURE AGREGAR_VENDEDOR
@PK_CED_VEN NCHAR(15),
@NOM_VEN NCHAR(20),
@TEL_VEN NCHAR(20),
@CEL_VEN NCHAR(20),
@DIR_VEN NCHAR(100),
@FEC_ING_VEN DATETIME
AS
BEGIN

INSERT INTO VENDEDORES (PK_CED_VEN,NOM_VEN,TEL_VEN,CEL_VEN,DIR_VEN,FEC_ING_VEN)
VALUES (@PK_CED_VEN, @NOM_VEN,@TEL_VEN,@CEL_VEN, @DIR_VEN, @FEC_ING_VEN)

END

EXEC AGREGAR_VENDEDOR '39168997','STELLA SANCHEZ', '3036536','3507906464','CALLE 124 SUR', '2020/05/20'

SELECT * FROM VENDEDORES