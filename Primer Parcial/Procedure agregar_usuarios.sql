CREATE PROCEDURE AGREGAR_USUARIOS
@PK_CED_CLI NCHAR(15),
@NOM_CLI NCHAR(50),
@TEL_CLI NCHAR (20),
@CEL_CLI NCHAR(20),
@DIR_CLI NCHAR (100),
@FEC_ING_CLI DATETIME

AS 
BEGIN
INSERT INTO CLIENTES (PK_CED_CLI, NOM_CLI, TEL_CLI, CEL_CLI, DIR_CLI, FEC_ING_CLI)
VALUES(@PK_CED_CLI, @NOM_CLI, @TEL_CLI, @CEL_CLI, @DIR_CLI, @FEC_ING_CLI)
END

